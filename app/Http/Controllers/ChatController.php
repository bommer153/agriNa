    public function sendMessage($id,Request $request)
    {
        if($request->ajax()){
            $sender = auth::user()->id;
            chat::insert([
                'transaction' => $id,
                'sender' => $sender,
                'message' => $request->message,
            ]);

            //$orders = transaction::with("chats")->where('user',$sender)->where('status','4')->get();     
            $data = chat::where("transaction",$id)->get();
            
            
            return response()->json([
                'data' => view('userView.chat',['data'=>$data,'auth'=>$sender])->render(),
            ]);
        }
    }

